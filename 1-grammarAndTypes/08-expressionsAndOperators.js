// Comparison Orperators

// Equal Operator
console.log("3" == 3);

// Strict Equal Operator
console.log("3" === 3);

// No Equal
console.log("3" !=3);

// Strict Not Equal
console.log("3" !==3);

// Greater Than
console.log(3>2);

// Less Than
console.log(2<3);

//Greater Than or Equal to
console.log(2>=3);

//Less Than or Equal to
console.log(2<=3);

// OR
console.log(2 || 3);
// And
2 && 3;



