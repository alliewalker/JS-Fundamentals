// Assignment operator
let x =5;

// Addition assignment
x += 1;
console.log(x) //x = x+1

// Subtraction Assignment

x-=1;
console.log(x);

// Multiplication Assignment
x *=1 // x = x times 1

// Division Assignment
x /= 1; // x = x divided by 1

//Remainder (or Modulus) Assignment
x %= 1; //x = x%1;

// Exponential Assignment
x**=1; // x=x** 1

